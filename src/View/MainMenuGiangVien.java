    private void loadDataTable_BangDanhSachLop() {
        danhSachLop.clear();
        //lay du lieu tu database
        String query = "SELECT * FROM Lop INNER JOIN GIANGVIEN ON Lop.MaGV = GIANGVIEN.MaGV "
                + "WHERE TenDangNhap = '"+ tenDangNhap +"'";
        danhSachLop = ConnectToSQLServer.layData_BangLop(query);

        jTable_DanhSachLop.setModel(new CustomTable_DanhSachLop(danhSachLop));
    }

    private void loadDataTable_BangDanhSachBaiKiemTra() {
        danhSachBaiKiemTra.clear(); 
        //lay du lieu tu database
        Iterator<Lop> it = danhSachLop.iterator();
        while(it.hasNext())
        {
            String query = "SELECT * FROM dbo.[BAIKIEMTRA] INNER JOIN dbo.[LOP] ON BAIKIEMTRA.MaLop = LOP.MaLop "
                    + "WHERE BaiKiemTra.MaLop = '" + it.next().getMaLop() + "'";
            ArrayList<BaiKiemTra> temp = ConnectToSQLServer.layData_BangBaiKiemTra(query);
            danhSachBaiKiemTra.addAll(temp);
        }

        jTable_DanhSachBaiKiemTra.setModel(new CustomTable_DanhSachBaiKiemTra(danhSachBaiKiemTra));
    }
