    private void jButton_XoaLopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_XoaLopActionPerformed
        // TODO add your handling code here:
        if(dongChon_Lop < 0)
        {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng nào để xóa", "Thông báo", JOptionPane.DEFAULT_OPTION);
            return;
        }
        
        Object[] options = { "Có", "Không" };
        //Check = 0: co 1: khong
        int check = JOptionPane.showOptionDialog(null, "Bạn chắc chắn muốn xóa ?", "Thông Báo",
                                    JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                                    null, options, options[0]);
        
        if(check == 0)
        {
            String maLop = jTable_DanhSachLop.getValueAt(dongChon_Lop, 0).toString();
            String query = "DELETE dbo.[LOP] WHERE MaLop= '"+maLop+"'";
            ConnectToSQLServer.thucHienLenhSQL(query);
            
            dongChon_Lop = -1;
            loadDataTable_BangDanhSachLop();
        }   
    }//GEN-LAST:event_jButton_XoaLopActionPerformed

